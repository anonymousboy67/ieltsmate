// Get a random word for the day (deterministic based on date)
export function getDailyWord(date: string): WordEntry {
  const dateNum = date.replace(/-/g, "").slice(-4)
  const index = parseInt(dateNum, 10) % WORD_BANK.length
  return WORD_BANK[index]
}

// Get words by topic
export function getWordsByTopic(topic: string): WordEntry[] {
  return WORD_BANK.filter((w) => w.topic === topic)
}

// Get words by band
export function getWordsByBand(band: number): WordEntry[] {
  return WORD_BANK.filter((w) => w.band >= band)
}
